#Many Values to Multiple Variables
a, b, c = 1, 2, 3
print(a)
print(b)
print(c)
print(a, b, c)


#One Value to Multiple Variables
m = n = o = "Apple"
print(m)
print(n)
print(o)


#Unpack a Collection
fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)
print(fruits)