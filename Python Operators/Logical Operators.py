# Logical "AND" Operator --------

x = 5

print(x > 3 and x < 10)

# returns True because 5 is greater than 3 AND 5 is less than 10


# Logical "OR" Operator --------
y = 5

print(x > 3 or y < 4)

# returns True because one of the conditions are true (5 is greater than 3, but 5 is not less than 4)


